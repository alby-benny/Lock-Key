function getCookie(cname) {
  let name = cname + "=";
  let ca = document.cookie.split(';');
  for(let i = 0; i < ca.length; i++) {
    let c = ca[i];
    while (c.charAt(0) == ' ') {
      c = c.substring(1);
    }
    if (c.indexOf(name) == 0) {
      return c.substring(name.length, c.length);
    }
  }
  return "";
}
var LokiTimer;
if (getCookie("Timer") == "") {
    LokiTimer=0;
}
else{
    LokiTimer=parseInt(getCookie("Timer"))
}

function updateTimer() {
    LokiTimer++;
}


interval = setInterval(updateTimer, 1000);

function setCookie(cname, cvalue) {
    document.cookie = cname + "=" + cvalue + ";";
}


function CreateFs(FsName){
    let FS=document.getElementById('fileSystem');
    FS.innerHTML='';
    for (i in FsName){
        if(FsName[i]=='folder'){
            let folder=document.createElement("div");
            folder.setAttribute("class","each-folder");
            folder.setAttribute("onclick","nextFs('"+i+"')");
            folder.innerHTML=`<img src="images/icons/folder.png">
                        <div id="FolderName">`+i+`</div>`;
            FS.appendChild(folder);
        }
        else if(FsName[i]=='file'){
            let file=document.createElement("div");
            file.setAttribute("class","each-file");
            file.innerHTML=`<img src="images/icons/pdf.png">
                        <div id="FileName">`+i+`</div>`;
            FS.appendChild(file);
        }
        else if(FsName[i]=='image2'){
            let file=document.createElement("div");
            file.setAttribute("class","each-image");
            file.setAttribute("onclick","Finish()");
            file.innerHTML=`<img src="images/Tesseract.png">
                        <div id="imageName">`+i+`</div>`;
            FS.appendChild(file);
        }
        else if(FsName[i]=='image'){
            let file=document.createElement("div");
            file.setAttribute("class","each-image");
            file.innerHTML=`<img src="images/replica.jpg">
                        <div id="imageName">`+i+`</div>`;
            FS.appendChild(file);
        }
    }
    if(FsName!=home){
        let Par=FsName["ParentFolder"];
        document.getElementById("back-btn").innerHTML=`<img src="images/icons/left-arrow.png" onclick="goBack('`+Par+`')">`;
    }
    else{
        document.getElementById("back-btn").innerHTML=`<img src="images/icons/left-arrow-grey.png">`;
    }
}
CreateFs(home);
function nextFs(FolderName){
    CreateFs(window[FolderName]);
}
function goBack(Par){
    if(window[Par]){
        CreateFs(window[Par]);
    }
}
function HomePage(){
    CreateFs(home);
}
function Finish(){
    clearInterval(interval);
    setCookie("Timer",LokiTimer);
    location.href="LokiResult.html";
}